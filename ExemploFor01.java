/**
 * ExemploFor01
 */
public class ExemploFor01 {

    public static void main(String[] args) {
        // 1ª parte serve para declaração
        // 2ª parte é a condição para o for ser executado
        // 3ª parte é o incremento ou decremento

        for(int atual = 0; atual < 10; atual = atual + 2){
            System.out.println(atual);
        }

        

    }
}